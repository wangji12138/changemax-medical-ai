package com.example.wangji.changemax.model.internal;

public class Organ {
	private int organ_id;
	private String organ_name;
	private String organ_own_part;

	public int getOrgan_id() {
		return organ_id;
	}

	public void setOrgan_id(int organ_id) {
		this.organ_id = organ_id;
	}

	public String getOrgan_name() {
		return organ_name;
	}

	public void setOrgan_name(String organ_name) {
		this.organ_name = organ_name;
	}

	public String getOrgan_own_part() {
		return organ_own_part;
	}

	public void setOrgan_own_part(String organ_own_part) {
		this.organ_own_part = organ_own_part;
	}

}
